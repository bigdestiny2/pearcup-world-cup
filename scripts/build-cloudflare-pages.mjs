#!/usr/bin/env node
// Build the single Cloudflare Pages artifact without mixing the app and the
// marketing source trees. `/` stays the public site; `/play/` is the runnable
// browser app. Runtime keys and local config are deliberately never copied.
import { cpSync, existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { createHash } from 'node:crypto'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const site = join(root, 'site')
const app = join(root, 'app')
const dist = join(root, 'cloudflare', 'pages-dist')
const check = process.argv.includes('--check')
const identityApi = normalizeApi(process.env.PEARCUP_IDENTITY_API_URL || '')
const relayApi = normalizeApi(process.env.PEARCUP_HIVERELAY_URL || '')
const liveDataApi = normalizeLiveDataApi(process.env.PEARCUP_LIVE_DATA_RELAY_URL || '')

if (check) {
  const required = ['index.html', 'play/index.html', 'play/pearcup-identity.js', 'play/assets/icon-256.png']
  const missing = required.filter(path => !existsSync(join(dist, path)))
  if (missing.length) throw new Error(`Cloudflare Pages build is missing: ${missing.join(', ')}`)
  const page = readFileSync(join(dist, 'play', 'index.html'), 'utf8')
  const match = page.match(/\.\/((?:public-runtime-settings\.)[a-f0-9]{12}\.js)/)
  if (!match || !existsSync(join(dist, 'play', match[1]))) throw new Error('Cloudflare Pages build is missing its content-addressed public runtime settings')
  const settings = readFileSync(join(dist, 'play', match[1]), 'utf8')
  if (identityApi && !settings.includes(identityApi)) throw new Error('Cloudflare Pages build has a stale identity API URL')
  if (relayApi && !settings.includes(relayApi)) throw new Error('Cloudflare Pages build has a stale HiveRelay URL')
  if (liveDataApi && !settings.includes(liveDataApi)) throw new Error('Cloudflare Pages build has a stale live-data relay URL')
  console.log('Cloudflare Pages build is current')
  process.exit(0)
}

rmSync(dist, { recursive: true, force: true })
mkdirSync(dist, { recursive: true })
cpSync(site, dist, { recursive: true, filter: keepMarketingFile })
mkdirSync(join(dist, 'play'), { recursive: true })
cpSync(app, join(dist, 'play'), { recursive: true, filter: keepAppFile })
const runtimeSettings = publicRuntimeSettings(identityApi, relayApi, liveDataApi)
const runtimeSettingsRevision = createHash('sha256').update(runtimeSettings).digest('hex').slice(0, 12)
const runtimeSettingsName = `public-runtime-settings.${runtimeSettingsRevision}.js`
writeFileSync(join(dist, 'play', runtimeSettingsName), runtimeSettings)
const pageFile = join(dist, 'play', 'index.html')
const page = readFileSync(pageFile, 'utf8')
if (!page.includes('./public-runtime-settings.js')) throw new Error('browser app is missing its public runtime settings script')
writeFileSync(pageFile, page.replace('./public-runtime-settings.js', `./${runtimeSettingsName}`))
writeFileSync(join(dist, 'play', '_headers'), headers(identityApi, liveDataApi))

console.log(`Cloudflare Pages build wrote ${dist}${identityApi ? ' with portable identity enabled' : ' with portable identity disabled'}${relayApi ? ' and HiveRelay live sync enabled' : ' and HiveRelay live sync disabled'}`)

function keepMarketingFile (source) {
  const relative = source.slice(site.length + 1)
  return relative !== '.DS_Store'
}

function keepAppFile (source) {
  const relative = source.slice(app.length + 1)
  if (!relative) return true
  if (relative.startsWith('node_modules/') || relative.startsWith('config/') || relative.startsWith('.git/')) return false
  if (relative.endsWith('.test.js') || relative.endsWith('.cjs') || relative.endsWith('.mjs')) return false
  if (relative === 'package.json') return false
  return !relative.endsWith('.DS_Store')
}

function normalizeApi (value) {
  if (!value) return ''
  const url = new URL(value)
  if (url.protocol !== 'https:' || url.username || url.password || url.search || url.hash) throw new Error('PEARCUP_IDENTITY_API_URL must be a public HTTPS origin without credentials')
  return url.href.replace(/\/$/, '')
}

function normalizeLiveDataApi (value) {
  if (!value) return ''
  const url = new URL(value)
  if (url.protocol !== 'https:' || url.username || url.password || url.search || url.hash || !/\.json$/i.test(url.pathname)) {
    throw new Error('PEARCUP_LIVE_DATA_RELAY_URL must be a public HTTPS JSON URL without credentials')
  }
  return url.href
}

function publicRuntimeSettings (apiUrl, relayUrl, liveDataUrl) {
  const peerRelay = relayUrl
    ? `{ enabled: true, relayUrl: ${JSON.stringify(relayUrl)}, service: 'outboxlog', protocol: 'pearcup-sync-v2' }`
    : 'null'
  const liveData = liveDataUrl
    ? `{ relayUrl: ${JSON.stringify(liveDataUrl)}, oddsRelayUrl: ${JSON.stringify(new URL('polymarket-odds.json', liveDataUrl).href)}, pollMs: 30000 }`
    : 'null'
  return `// Generated by scripts/build-cloudflare-pages.mjs. Public values only.\n(function attachPearCupPublicRuntimeSettings (root) {\n  const settings = {\n    liveData: ${liveData},\n    peerRelay: ${peerRelay},\n    identity: { enabled: ${apiUrl ? 'true' : 'false'}, apiUrl: ${JSON.stringify(apiUrl)} }\n  }\n  root.PearCupPublicRuntimeSettings = settings\n  if (typeof window !== 'undefined' && window !== root) window.PearCupPublicRuntimeSettings = settings\n})(typeof globalThis !== 'undefined' ? globalThis : window)\n`
}

function headers (apiUrl, liveDataUrl) {
  const liveDataOrigin = liveDataUrl ? new URL(liveDataUrl).origin : ''
  const connect = [apiUrl, liveDataOrigin, "'self'", 'https:'].filter(Boolean).join(' ')
  return `/play/*\n  X-Content-Type-Options: nosniff\n  Referrer-Policy: no-referrer\n  Permissions-Policy: camera=(), geolocation=(), payment=()\n  Cross-Origin-Opener-Policy: same-origin\n  Content-Security-Policy: default-src 'self'; script-src 'self' 'unsafe-inline' 'unsafe-eval'; style-src 'self' 'unsafe-inline'; img-src 'self' data: blob: https://crests.football-data.org; media-src 'self' blob:; connect-src ${connect}; object-src 'none'; base-uri 'none'\n`
}
